    /**
     * 保存新建的资源
     *
     * @param  \think\Request  $request
     * @return \think\Response
     */
    public function save(Request $request)
    {
        $data=$this->request->post();
        $model=model('Team');
        $result=$model->inserts($data);
        if($result){
           return json([
               'code'=>config('code.success'),
               'msg'=>'团队添加成功'
           ]);
        }else{
            return json([
                'code'=>config('code.fail'),
                'msg'=>'团队添加失败'
            ]);
        }
    }

    /**
     * 显示指定的资源
     *
     * @param  int  $id
     * @return \think\Response
     */
    public function read($id)
    {
        //
        $model=model('Team');
        $result=$model->finds($id);
        if($result){
            return json([
                'code'=>config('code.success'),
                'msg'=>'团队展示成功',
                'data'=>$result
            ]);
        }else{
            return json([
                'code'=>config('code.fail'),
                'msg'=>'团队展示失败',
                'data'=>[]
            ]);
        }
    }

