    }

    /**
     * 保存更新的资源
     *
     * @param  \think\Request  $request
     * @param  int  $id
     * @return \think\Response
     */
    public function update(Request $request, $id)
    {
        $data=$this->request->put();
        $model=model('Team');
        $team=$model->queryones($data);
        if($team){
            return json([
                'code'=>config('code.fail'),
                'msg'=>'团队信息已存在，未能做修改'
            ]);
        }
        $result=$model->updates($data,$id);
        if($result){
            return json([
                'code'=>config('code.success'),
                'msg'=>'修改成功'
            ]);
        }else{
            return json([
                'code'=>config('code.fail'),
                'msg'=>'修改失败'
            ]);
        }
    }

    /**
     * 删除指定资源
     *
     * @param  int  $id
     * @return \think\Response
     */
    public function delete($id)
    {
        //
        $model=model('Team');
        $result=$model->deletes($id);
        if($result){
            return json([
                'code'=>config('code.success'),
                'msg'=>'删除成功'
            ]);
        }else{
            return json([
                'code'=>config('code.fail'),
                'msg'=>'删除失败'
            ]);
        }
    }
